import folium

def create_map():
    # Создаем базовую карту мира, используя тайлы Google Maps на русском языке
    google_ru_tiles = "https://mt1.google.com/vt/lyrs=m&x={x}&y={y}&z={z}&hl=ru"
    
    my_map = folium.Map(
        location=[20, 0], 
        zoom_start=2, 
        tiles=google_ru_tiles, 
        attr="Google Maps" # Обязательный параметр авторства при использовании сторонних тайлов
    )

    # --- ДОБАВЛЯЕМ ПОЛУОСТРОВА ---
    # Словарь с названиями полуостровов и их примерными координатами [широта, долгота]
    peninsulas = {
        "Крымский": [45.3, 34.4],
        "Гибралтар": [36.14, -5.35],
        "Пиренейский": [40.0, -4.0],
        "Аппенинский": [42.0, 14.0],
        "Скандинавский": [63.0, 14.0],
        "Аравийский": [23.0, 46.0],
        "Индокитай": [15.0, 102.0],
        "Индостан": [20.0, 77.0],
        "Малая Азия": [39.0, 32.0],
        "Сомали": [8.0, 48.0],
        "Аляска": [64.0, -150.0],
        "Лабрадор": [54.0, -64.0],
        "Калифорния": [28.0, -113.5],
        "Флорида": [28.0, -81.5],
        "Юкатан": [19.5, -89.0]
    }

    # Проходим циклом по всем полуостровам и добавляем их на карту
    for name, coords in peninsulas.items():
        folium.Marker(
            location=coords,
            popup=f"<b>Полуостров {name}</b>",
            tooltip=name,
            # Изменили цвет маркера на фиолетовый ('purple')
            icon=folium.Icon(color="purple", icon="info-sign")
        ).add_to(my_map)

    # Сохраняем результат
    my_map.save("geography_map.html")
    print("Карта с полуостровами готова! Открой файл geography_map.html в браузере.")

# Запускаем нашу функцию
create_map()